fn active_keymap() -> &'static Mutex<[u32; 96]> {
    static KEYMAP: OnceLock<Mutex<[u32; 96]>> = OnceLock::new();
    KEYMAP.get_or_init(|| Mutex::new(DEFAULT_KEYMAP))
}

fn encode_remap_packet(map: &[u32; 96]) -> [u8; 520] {
    let mut packet = TEMPLATE_03;
    for (index, value) in map.iter().enumerate() {
        let offset = 8 + index * 4;
        packet[offset..offset + 4].copy_from_slice(&value.to_be_bytes());
    }
    packet
}

fn usage_value_for_vk(vk: u16) -> Option<u32> {
    if vk == 0 {
        return Some(0);
    }
    KEYS.iter()
        .find(|key| key.vk == vk)
        .map(|key| DEFAULT_KEYMAP[key.led_index as usize])
}

/// Brightness offset for an effect in the speed/brt table.
/// Formula confirmed from breathing (hw=2→0x044) and snake (hw=11→0x056) captures.
pub fn effect_brightness_offset(effect: Effect) -> usize {
    0x044usize.saturating_add((effect as u8 as usize).saturating_sub(2) * 2)
}

pub fn effect_speed_offset(effect: Effect) -> usize {
    effect_brightness_offset(effect) + 1
}

/// Colour triplet offset in TEMPLATE_0A for animated effects.
/// Formula confirmed from breathing (hw=2→0x032) and ripple (hw=7→0x09B) captures.
pub fn effect_color_offset(effect: Effect) -> usize {
    0x1d + (effect as u8 as usize).saturating_sub(1) * 21
}

// ── Speed and brightness ───────────────────────────────────────────────────

pub mod speed {
    /// Speed byte: upper nibble = speed (0–4), lower nibble = colour mode
    /// (0x0 = single colour, 0x7 = random RGB cycling).
    pub const MID: u8 = 0x27;
    pub fn from_step(step: u8) -> u8 {
        match step.min(4) { 0=>0x07, 1=>0x17, 2=>0x27, 3=>0x37, _=>0x47 }
    }
}

pub mod brightness {
    pub const MAX: u8 = 0x04;
}

// ── Effect IDs ─────────────────────────────────────────────────────────────

#[repr(u8)]
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum Effect {
    Off           = 0x00,
    Static        = 0x01,
    Breathing     = 0x02,
    SpectrumCycle = 0x03,
    FlashAway     = 0x04, // packet ID confirmed from OEM capture
    Raindrops     = 0x05,
    Ripple        = 0x07,
    Twinkle       = 0x08,
    Reaction      = 0x0C,
    SineWave      = 0x0D,
    Rotating      = 0x0F,
    Waterfall     = 0x10,
    PerKeyRgb     = 0x15,
}

// ── Types ──────────────────────────────────────────────────────────────────

#[derive(Debug, Clone, Copy, PartialEq, Eq, serde::Serialize, serde::Deserialize)]
pub struct Rgb { pub r: u8, pub g: u8, pub b: u8 }

impl Rgb {
    pub const WHITE: Self = Self { r: 255, g: 255, b: 255 };
    pub const OFF:   Self = Self { r: 0,   g: 0,   b: 0   };
    pub fn new(r: u8, g: u8, b: u8) -> Self { Self { r, g, b } }
}

pub type LedMap = [Rgb; 96];

#[derive(Debug, Clone)]
pub struct EffectParams {
    pub effect:       Effect,
    pub color:        Rgb,
    pub speed:        u8,
    pub brightness:   u8,
    pub direction:    u8,
    pub random_color: bool,
}

impl Default for EffectParams {
    fn default() -> Self {
        Self {
            effect: Effect::SpectrumCycle, color: Rgb::WHITE,
            speed: speed::MID, brightness: brightness::MAX,
            direction: 0x01, random_color: false,
        }
    }
}

// ── Keyboard ───────────────────────────────────────────────────────────────

pub struct Keyboard { device: HidDevice }

impl Keyboard {
    pub fn open()          -> Result<Self> { Self::open_vid_pid(VID, PID) }
    pub fn open_wireless() -> Result<Self> { Self::open_vid_pid(VID_WIRELESS, PID_WIRELESS) }

    fn open_vid_pid(vid: u16, pid: u16) -> Result<Self> {
        let api = HidApi::new().context("Failed to initialise HIDAPI")?;
        let mut candidates: Vec<_> = api.device_list()
            .filter(|d| d.vendor_id() == vid && d.product_id() == pid)
            .collect();
        candidates.sort_by_key(|d| if d.usage_page() == 0xFF00 { 0u8 } else { 1u8 });
        for info in &candidates {
            let Ok(dev) = info.open_device(&api) else { continue };
            if dev.send_feature_report(&TEMPLATE_84).is_ok() {
                eprintln!("Opened: interface={} usage_page={:#06x} path={}",
                    info.interface_number(), info.usage_page(),
                    info.path().to_string_lossy());
                return Ok(Self { device: dev });
            }
        }
        anyhow::bail!(
            "Cannot find a working HID interface for VID={:#06x} PID={:#06x}. \
             Tried {} interface(s). Run `nighthawk75 list-devices`. \
             Make sure you are running as Administrator.",
            vid, pid, candidates.len()
        )
    }

    pub fn list_devices() {
        let api = match HidApi::new() {
            Ok(a) => a,
            Err(e) => { eprintln!("HidApi init failed: {e}"); return; }
        };
        println!("HID interfaces for VID=0x{VID:04X} PID=0x{PID:04X}:");
        let mut found = false;
        for d in api.device_list().filter(|d| d.vendor_id() == VID && d.product_id() == PID) {
            found = true;
            println!("  interface={:2}  usage_page={:#06x}  usage={:#06x}  path={}",
                d.interface_number(), d.usage_page(), d.usage(),
                d.path().to_string_lossy());
        }
        if !found { println!("  (none found — is the keyboard plugged in?)"); }
    }

    fn send(&self, data: &[u8]) -> Result<()> {
        self.device.send_feature_report(data).context("send_feature_report failed")?;
        Ok(())
    }

    fn send_lighting(&self, params: &EffectParams) -> Result<()> {
        // 0x0a: colour data
        let mut pkt0a = TEMPLATE_0A.to_vec();
        if matches!(params.effect, Effect::Static) {
            // Static: fill all 161 triplets with the chosen colour
            for i in 0..161usize {
                let off = 0x1d + i * 3;
                if off + 2 < pkt0a.len() {
                    pkt0a[off] = params.color.r; pkt0a[off+1] = params.color.g; pkt0a[off+2] = params.color.b;
                }
            }
        } else if !matches!(params.effect, Effect::SpectrumCycle | Effect::Off) {
            // Animated effects: colour at formula offset = 0x1d + (hw_id-1)*21
            let co = effect_color_offset(params.effect);
            if co + 2 < pkt0a.len() {
                pkt0a[co] = params.color.r; pkt0a[co+1] = params.color.g; pkt0a[co+2] = params.color.b;
            }
        }

        let brt_off = effect_brightness_offset(params.effect);
        let spd_off = effect_speed_offset(params.effect);
        // Speed lower nibble: 0x0 = single colour, 0x7 = random RGB cycling
        let spd_byte = if params.random_color || matches!(params.effect, Effect::SpectrumCycle) {
            params.speed | 0x07
        } else {
            params.speed & 0xF0
        };

        // 0x84b: pre-commit
        let mut pkt84b = TEMPLATE_84B.to_vec();
        pkt84b[OFF_EFFECT] = params.effect as u8;
        if brt_off < pkt84b.len() { pkt84b[brt_off] = params.brightness; }
        if spd_off < pkt84b.len() { pkt84b[spd_off] = spd_byte; }

        // 0x04: effect config
        let mut pkt04 = TEMPLATE_04.to_vec();
        pkt04[OFF_EFFECT] = params.effect as u8;
        if brt_off < pkt04.len() { pkt04[brt_off] = params.brightness; }
        if spd_off < pkt04.len() { pkt04[spd_off] = spd_byte; }

        self.send(&pkt0a)?;
        self.send(&TEMPLATE_84.to_vec())?;
        self.send(&pkt84b)?;
        self.send(&pkt04)?;
        Ok(())
    }

    pub fn set_effect(&self, params: &EffectParams) -> Result<()> {
        self.send_lighting(params)
    }

    pub fn set_static_color(&self, color: Rgb, brightness: u8) -> Result<()> {
        self.send_lighting(&EffectParams {
            effect: Effect::Static, color, brightness, random_color: false, ..Default::default()
        })
    }

    pub fn set_lights_off(&self) -> Result<()> {
        self.send_lighting(&EffectParams {
            effect: Effect::Off, color: Rgb::OFF, random_color: false, ..Default::default()
        })
    }

    pub fn set_per_key_rgb(&self, map: &LedMap) -> Result<()> {
        let mut pkt06 = TEMPLATE_06.to_vec();
        for (i, color) in map.iter().enumerate() {
            if i < PERKEY_PLANE_SIZE {
                pkt06[PERKEY_DATA_START + i]                      = color.r;
                pkt06[PERKEY_DATA_START + PERKEY_PLANE_SIZE + i]  = color.g;
                pkt06[PERKEY_DATA_START + PERKEY_PLANE_SIZE*2 + i] = color.b;
            }
        }
        let brt_off = effect_brightness_offset(Effect::PerKeyRgb);
        let spd_off = effect_speed_offset(Effect::PerKeyRgb);
        let mut pkt84b = TEMPLATE_84B.to_vec();
        pkt84b[OFF_EFFECT] = PERKEY_EFFECT_ID;
        pkt84b[0x11] = 0x01;
        if brt_off < pkt84b.len() { pkt84b[brt_off] = 0x07; }
        if spd_off < pkt84b.len() { pkt84b[spd_off] = 0x47; }
        let mut pkt04 = TEMPLATE_04.to_vec();
        pkt04[OFF_EFFECT] = PERKEY_EFFECT_ID;
        pkt04[0x11] = 0x01;
        if brt_off < pkt04.len() { pkt04[brt_off] = 0x07; }
        if spd_off < pkt04.len() { pkt04[spd_off] = 0x47; }
        self.send(&pkt06)?;
        self.send(&TEMPLATE_84.to_vec())?;
        self.send(&pkt84b)?;
        self.send(&pkt04)?;
        Ok(())
    }

    pub fn remap_key(&self, from_led: u8, to_vk: u16) -> Result<()> {
        let from_index = from_led as usize;
        if from_index >= DEFAULT_KEYMAP.len() {
            anyhow::bail!("remap_key: LED index out of range: {}", from_led);
        }
        let to_value = usage_value_for_vk(to_vk)
            .ok_or_else(|| anyhow!("remap_key: unsupported VK code 0x{:04X}", to_vk))?;

        let mut map = active_keymap()
            .lock()
            .map_err(|_| anyhow!("remap_key: keymap mutex poisoned"))?;
        map[from_index] = to_value;

        let pkt03 = encode_remap_packet(&map);
        self.send(&pkt03)?;
        Ok(())
    }
}
