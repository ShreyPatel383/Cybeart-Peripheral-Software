/// Key remap packet. Payload is a 96-entry big-endian keymap table indexed by LED slot.
pub const TEMPLATE_03: [u8; 520] = {
    let mut packet = [0u8; 520];
    packet[0] = 0x06;
    packet[1] = 0x03;
    packet[4] = 0x01;
    packet[7] = 0x02;
    packet[518] = 0x5A;
    packet[519] = 0xA5;
    packet
};

